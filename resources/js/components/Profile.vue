      },
      methods:{
              getProfilePhoto(){
                    let photo = "img/profile.png";
                    if (this.form.photo) {
                photo = (this.form.photo.length > 200 ) ? this.form.photo: "img/profile/"+this.form.photo
                    }
                    return photo
              },
              updateProfile(e){

                  let file = e.target.files[0]
                  let reader = new FileReader()
                  if(file['size'] < 2097152){
                  reader.onloadend = (file) => {
                      this.form.photo = reader.result
                  }
                  reader.readAsDataURL(file);
                  }else{
                    Swal.fire("Ooops!", "File size must not exceed 2 MB", "error")
                  }
              },
            updateInfo(){
              this.$Progress.start()
              
                this.form.put('api/profile/')
                .then(() => {
                  $("#userModal").modal("hide")
                    Fire.$emit('RefreshSidebar')
                    Swal.fire("Updated!", "Profile been Updated.", "success");
                   
                  this.$Progress.finish()

                })
                .catch(() => {
                  Swal.fire("Failed!", "There was something wrong", "warning")
                  this.$Progress.fail()
                })
            },
      },  
    }
